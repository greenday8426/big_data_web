setwd("c:\\easy_r")
install.packages("KoNLP")
install.packages("wordcloud")
library(KoNLP)
library(wordcloud)
txt<-readLines("job_40.txt")
nouns<-sapply(txt,extractNoun,USE.NAMES = F)
head(unlist(nouns),30)
nouns<-Filter(function(x){nchar(x)>=2},nouns)
write(unlist(nouns),"job_40_2.txt")
rev<-read.table("job_40_2.txt")
nrow(rev)
wordcount<-table(rev)
head(sort(wordcount,decreasing = T),30)
library(RColorBrewer)
palete<-brewer.pal(9,"Set1")
wordcloud(names(wordcount),freq=wordcount,scale=c(5,0.5),rot.per = 0.25,min.freq=1,random.order = F,random.color=T,colors=palete)
legend(0.3,1, "40대 창업 관련 분석",cex=0.8,fill=NA,border=NA,bg="white",text.col="red",text.font=600,box.col="red")
